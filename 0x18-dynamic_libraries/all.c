#include <stdio.h>

/**
 * add - add two integers
 * @a: first int
 * @b: second int
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * subtract - subtract two integers
 * @a: first int
 * @b: second int
 * Return: subtracted final result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mod - modulous two integers
 * @a: first int
 * @b: second int
 * Return: modulous
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * mul - multiplies two integers
 * @a: first int
 * @b: second int
 * Return: multiply
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two integers
 * @a: first int
 * @b: second int
 * Return: divide
 */
int div(int a, int b)
{
	return (a / b);
}
